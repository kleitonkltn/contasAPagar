    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ContasDAO condao = null;
        try {
            condao = new ContasDAO();
        } catch (SQLException ex) {
            Logger.getLogger(Contas.class.getName()).log(Level.SEVERE, null, ex);
        }
        String dataE = emissao.getText();
        String dataV = Vencimento.getText();

        java.util.Date dataEmissao = null;
        java.util.Date dataVencimento = null;
        try {
            dataEmissao = formatador.parse(dataE);
            dataVencimento = formatador.parse(dataV);
        } catch (ParseException ex) {
            Logger.getLogger(ContasS.class.getName()).log(Level.SEVERE, null, ex);
        }

        java.sql.Date dataSql = new java.sql.Date(dataEmissao.getTime());
        java.sql.Date data = new java.sql.Date(dataVencimento.getTime());

        Conta c = new Conta();
        c.setDescricao(descricao.getText());
        c.setEmpresa(empresa.getText());
        c.setTipoConta(tipoConta);
        c.setFormaDePg(formaPg.getText());
        c.setEmissao(dataSql);
        c.setValorTotal(Double.parseDouble(valor.getText()));
        c.setVencimento(data);

        System.out.println(c.getDescricao());
        System.out.println(c.getEmpresa());
        System.out.println(c.getTipoConta());
        System.out.println(c.getFormaDePg());
        System.out.println(c.getEmissao());
        System.out.println(c.getValorTotal());
        System.out.println(c.getVencimento());

        try {
            condao.AdicionarContas(c);

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        limpardados();
        try {
            carregarTabela("");
        } catch (SQLException ex) {
            Logger.getLogger(ContasS.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton2ActionPerformed


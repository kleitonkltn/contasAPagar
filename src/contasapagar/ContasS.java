    public void carregarTabela(String status) throws SQLException {
        String sql = "";
        if (status.equals("contaAPagar")) {
            sql = "select * from contas where tipoconta = 'Conta a pagar'";
        } else if (status.equals("contaAReceber")) {
            sql = "select * from contas where tipoconta = 'Conta a  receber'";
        } else {
            sql = "select * from contas";
        }

        PreparedStatement stmt = null;
        try {
            stmt = conn.prepareStatement(sql);
        } catch (SQLException ex) {
            Logger.getLogger(Contas.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            stmt = conn.prepareStatement(sql);
        } catch (SQLException ex) {
            Logger.getLogger(Contas.class.getName()).log(Level.SEVERE, null, ex);
        }
        ResultSet resultado = stmt.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);
        if (model.getColumnCount() == 0) {
            model.addColumn("Descricao");
            model.addColumn("empresa");
            model.addColumn("forma Pg");
            model.addColumn("Tipo da conta");
            model.addColumn("Emissao");
            model.addColumn("valor");
            model.addColumn("Vencimento");

        }
        while (resultado.next()) {
            model.addRow(new Object[]{
                resultado.getString("descricao"),
                resultado.getString("empresa"),
                resultado.getString("formaPagamento"),
                resultado.getString("tipoconta"),
                resultado.getString("emissao"),
                resultado.getString("valor"),
                resultado.getString("vencimento")
            });
        }
        stmt.close();
    }

